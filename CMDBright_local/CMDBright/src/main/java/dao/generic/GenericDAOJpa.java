package dao.generic;

import java.io.Serializable;
import java.util.List;

import javax.persistence.EntityManager;
import javax.persistence.PersistenceContext;
import javax.persistence.criteria.CriteriaBuilder;
import javax.persistence.criteria.CriteriaQuery;

public class GenericDAOJpa< T, ID extends Serializable > implements IGenericDAO< T, ID >{

    private Class<T> clazz;

    @PersistenceContext
    protected EntityManager entityManager;

    public final void setClazz(final Class<T> clazzToSet) {
        this.clazz = clazzToSet;
    }

    public T findOne(final ID id) {
        return entityManager.find(clazz, id);
    }

    @SuppressWarnings("unchecked")
    public List<T> findAll() {
        return entityManager.createQuery("from " + clazz.getName()).getResultList();
    }

    public void create(final T entity) {
        entityManager.persist(entity);
    }

    public T update(final T entity) {
        return entityManager.merge(entity);
    }

    public void delete(final T entity) {
        entityManager.remove(entity);
    }

    public void deleteById(final ID entityId) {
        final T entity = findOne(entityId);
        delete(entity);
    }
    public CriteriaBuilder getCriteriaBuilder() {
    	return entityManager.getCriteriaBuilder();
    }
    /*    public List<T> findByQuery(final String queryString) throws Exception {
    final List<T> results = entityManager.find(clazz, queryString);
    return results;
	}*/
    public List<T> findByCriteria(final CriteriaQuery<T> c) {
    	return entityManager.createQuery(c).getResultList();
    }
    
}